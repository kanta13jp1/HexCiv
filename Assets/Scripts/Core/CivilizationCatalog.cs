        public static IReadOnlyList<CivilizationDef> All => Definitions;

        public static CivilizationDef Find(string id)
        {
            if (string.IsNullOrEmpty(id)) return null;
            for (int i = 0; i < Definitions.Length; i++)
                if (string.Equals(Definitions[i].Id, id, StringComparison.OrdinalIgnoreCase))
                    return Definitions[i];
            return null;
        }

        public static CivilizationDef FindByName(string nameJa)
        {
            if (string.IsNullOrEmpty(nameJa)) return null;
            for (int i = 0; i < Definitions.Length; i++)
                if (string.Equals(Definitions[i].NameJa, nameJa, StringComparison.Ordinal))
                    return Definitions[i];
            return null;
        }

        public static CivilizationDef DefaultForSlot(int slot)
        {
            if (Definitions.Length == 0) return null;
            int index = ((slot % Definitions.Length) + Definitions.Length) % Definitions.Length;
            return Definitions[index];
        }

        static CivilizationDef Civ(string id, string nameJa, string regionJa, string eraJa,
            byte red, byte green, byte blue, params string[] cityNames)
        {
            return new CivilizationDef(id, nameJa, regionJa, eraJa, red, green, blue, cityNames);
        }
