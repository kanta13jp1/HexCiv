        public static IReadOnlyList<HeritageSiteDef> All => Definitions;

        public static HeritageSiteDef Find(string id)
        {
            if (string.IsNullOrEmpty(id)) return null;
            for (int i = 0; i < Definitions.Length; i++)
                if (string.Equals(Definitions[i].Id, id, StringComparison.OrdinalIgnoreCase))
                    return Definitions[i];
            return null;
        }

        public static List<HeritageSiteDef> ForRegion(string regionJa)
        {
            var result = new List<HeritageSiteDef>();
            for (int i = 0; i < Definitions.Length; i++)
                if (string.IsNullOrEmpty(regionJa) || string.Equals(Definitions[i].RegionJa, regionJa,
                    StringComparison.Ordinal))
                    result.Add(Definitions[i]);
            return result;
        }

